void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim){
	if(htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1){
		if(IsFirstCaptured1 == 0){
			IC_Val11 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);
			IsFirstCaptured1 = 1;
			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_FALLING);
		}
		else if(IsFirstCaptured1 == 1){
			IC_Val12 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);
			htim->Instance->CNT = 0;
			if(IC_Val12 > IC_Val11) Difference1 = IC_Val12 - IC_Val11;
			else if(IC_Val11> IC_Val12) Difference1 = (0xffff - IC_Val11) + IC_Val12;
			Distance1 = Difference1 * 0.034 / 2;
			IsFirstCaptured1 = 0;

			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_RISING);
			__HAL_TIM_DISABLE_IT(htim, TIM_IT_CC1);
		}
	}
	if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_2) {
		if (IsFirstCaptured2 == 0) {
			IC_Val21 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_2);
			IsFirstCaptured2 = 1;
			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_2,TIM_INPUTCHANNELPOLARITY_FALLING);
		} else if (IsFirstCaptured2 == 1) {
			IC_Val22 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_2);
			htim->Instance->CNT = 0;
			if (IC_Val22 > IC_Val21)
				Difference2 = IC_Val22 - IC_Val21;
			else if (IC_Val21 > IC_Val22)
				Difference2 = (0xffff - IC_Val21) + IC_Val22;
			Distance2 = Difference2 * 0.034 / 2;
			IsFirstCaptured2 = 0;

			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_2, TIM_INPUTCHANNELPOLARITY_RISING);
			__HAL_TIM_DISABLE_IT(htim, TIM_IT_CC2);
		}
	}
	if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_3) {
		if (IsFirstCaptured3 == 0) {
			IC_Val31 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_3);
			IsFirstCaptured3 = 1;
			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_3,
					TIM_INPUTCHANNELPOLARITY_FALLING);
		} else if (IsFirstCaptured3 == 1) {
			IC_Val32 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_3);
			htim->Instance->CNT = 0;
			if (IC_Val32 > IC_Val31)
				Difference3 = IC_Val32 - IC_Val31;
			else if (IC_Val31 > IC_Val32)
				Difference3 = (0xffff - IC_Val31) + IC_Val32;
			Distance3 = Difference3 * 0.034 / 2;
			IsFirstCaptured3 = 0;

			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_3,
					TIM_INPUTCHANNELPOLARITY_RISING);
			__HAL_TIM_DISABLE_IT(htim, TIM_IT_CC3);
		}
	}
}

